    private void actualizarTabla(){
        String [] encabezado = new String[5];
        encabezado[0]="Nombre";
        encabezado[1]="Edad";
        encabezado[2]="Cedula";
        encabezado[3]="Curso";
        encabezado[4]="Lugar";
        
        var datos = new Object[this.estudianteControl.listar().size()][5]; 
        
        var i=0; 
        for(var estudiante:this.estudianteControl.listar()){
            datos[i][0]=estudiante.getNombre(); 
            datos[i][1]=estudiante.getEdad(); 
            datos[i][2]=estudiante.getNumeroCedula(); 
            datos[i][3]=estudiante.getGrado();
            datos[i][4]=estudiante.getLugarNacimiento();
            i++; 
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
    
    }
